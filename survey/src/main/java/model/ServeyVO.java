package model;

public class ServeyVO {
	private int surveyIdx;
	private String startDate;
	private String endDate;
	private int state;
	private int resultCondition;
	private int personLimit;
	private int duplication;
	private int dividePate;
	private int questionNo;
	private String password;
	public int getSurveyIdx() {
		return surveyIdx;
	}
	public void setSurveyIdx(int surveyIdx) {
		this.surveyIdx = surveyIdx;
	}
	public String getStartDate() {
		return startDate;
	}
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	public String getEndDate() {
		return endDate;
	}
	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
	public int getState() {
		return state;
	}
	public void setState(int state) {
		this.state = state;
	}
	public int getResultCondition() {
		return resultCondition;
	}
	public void setResultCondition(int resultCondition) {
		this.resultCondition = resultCondition;
	}
	public int getPersonLimit() {
		return personLimit;
	}
	public void setPersonLimit(int personLimit) {
		this.personLimit = personLimit;
	}
	public int getDuplication() {
		return duplication;
	}
	public void setDuplication(int duplication) {
		this.duplication = duplication;
	}
	public int getDividePate() {
		return dividePate;
	}
	public void setDividePate(int dividePate) {
		this.dividePate = dividePate;
	}
	public int getQuestionNo() {
		return questionNo;
	}
	public void setQuestionNo(int questionNo) {
		this.questionNo = questionNo;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	
}
