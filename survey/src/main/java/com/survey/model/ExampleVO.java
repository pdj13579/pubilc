package com.survey.model;

public class ExampleVO {
	private int exampleIdx;
	private int mediaIdx;
	private String content;
	private int branch;
	private int exampleOrder;
	private int questionIdx;
	private int etc;
	private String preText;
	private String postText;
	private int weight;
	private int branchTo;
	public int getExampleIdx() {
		return exampleIdx;
	}
	public void setExampleIdx(int exampleIdx) {
		this.exampleIdx = exampleIdx;
	}
	public int getMediaIdx() {
		return mediaIdx;
	}
	public void setMediaIdx(int mediaIdx) {
		this.mediaIdx = mediaIdx;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public int getBranch() {
		return branch;
	}
	public void setBranch(int branch) {
		this.branch = branch;
	}
	public int getExampleOrder() {
		return exampleOrder;
	}
	public void setExampleOrder(int exampleOrder) {
		this.exampleOrder = exampleOrder;
	}
	public int getQuestionIdx() {
		return questionIdx;
	}
	public void setQuestionIdx(int questionIdx) {
		this.questionIdx = questionIdx;
	}
	public int getEtc() {
		return etc;
	}
	public void setEtc(int etc) {
		this.etc = etc;
	}
	public String getPreText() {
		return preText;
	}
	public void setPreText(String preText) {
		this.preText = preText;
	}
	public String getPostText() {
		return postText;
	}
	public void setPostText(String postText) {
		this.postText = postText;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	public int getBranchTo() {
		return branchTo;
	}
	public void setBranchTo(int branchTo) {
		this.branchTo = branchTo;
	}
	
}
